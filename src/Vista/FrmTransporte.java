    private void btnRegistrarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnRegistrarMouseClicked
        Transporte t;
        t = new Transporte(txtNPlacas.getText(), txtColor.getText(), txtModelo.getText(),
        cbxDestino.getSelectedItem().toString(),Integer.parseInt(txtNPasajeros.getText()));
             
        try {
            pro.agregar(t);
            listar();
        } catch (IOException e) {
        }
    }//GEN-LAST:event_btnRegistrarMouseClicked

    private void btnBuscarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnBuscarMouseClicked
        String cod = txtNPlacas.getText();
            
            for(Transporte t:pro.lista)
            {
                if(cod.equals(t.getNPlaca()))
                {
                    txtColor.setText(t.getCol());
                    txtModelo.setText(t.getMod());
                    txtNPasajeros.setText(String.valueOf(t.getNPasajeros()));
                    if(t.getDest().equals("ICA"))
                        cbxDestino.setSelectedIndex(1);
                    else if(t.getDest().equals("AREQUIPA"))
                        cbxDestino.setSelectedIndex(2);
                    else if(t.getDest().equals("TACNA"))
                        cbxDestino.setSelectedIndex(3);
                    else if(t.getDest().equals("TRUJILLO"))
                        cbxDestino.setSelectedIndex(4);
                    else if(t.getDest().equals("PIURA"))
                        cbxDestino.setSelectedIndex(5);
                }
            }
    }//GEN-LAST:event_btnBuscarMouseClicked

