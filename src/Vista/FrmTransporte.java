    private void listar() {
        //Metodo listar
        modelo.setRowCount(0);
        try {
            pro.leer();
            for (Transporte p : pro.lista) {
                Object[] fila = {p.getNPlaca(), p.getCol(), p.getMod(), p.getDest(),
                    p.getNPasajeros(), p.PrecioPasje(), p.PrecioCombustible(), p.montoRecaudado(), p.Ganancias()};
                modelo.addRow(fila);
            }
        } catch (IOException e) {
        }
    }
